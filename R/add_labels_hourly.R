add_labels_hourly <- function(hourly) {
  attr(hourly$meta_bat_volt, "label") <- "Battery voltage"
  attr(hourly$meta_needs_review, "label") <- "Data quality flag"
  attr(hourly$meta_station_id, "label") <- "Station ID"
  attr(hourly$meta_station_name, "label") <- "Station"
  # attr(hourly$meta_version, "label") <- 
  attr(hourly$date_datetime, "label") <- "Datetime"
